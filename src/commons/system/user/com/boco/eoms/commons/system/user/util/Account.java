package com.boco.eoms.commons.system.user.util;

public class Account {

	// 用户id
	private String accId;
	// 用户名称
	private String name;
	// 用户邮箱
	private String email;
	// 用户密码
	private String userPassword;
	// 用户性别
	private String gender;
	// 用户电话
	private String mobile;
	// 用户工号
	private String employeeNumber;
	// 用户部门id
	private String o;
	// 用户部门名称
	private String supporterDept;
	// 用户传真号
	private String facsimileTelephoneNumber;
	// 用户手机
	private String telephoneNumber;
	// 账号修改时间
	private String passwordModifiedDate;

	public String getAccId() {
		return accId;
	}

	public void setAccId(String accId) {
		this.accId = accId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getUserPassword() {
		return userPassword;
	}

	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getEmployeeNumber() {
		return employeeNumber;
	}

	public void setEmployeeNumber(String employeeNumber) {
		this.employeeNumber = employeeNumber;
	}

	public String getO() {
		return o;
	}

	public void setO(String o) {
		this.o = o;
	}

	public String getSupporterDept() {
		return supporterDept;
	}

	public void setSupporterDept(String supporterDept) {
		this.supporterDept = supporterDept;
	}

	public String getFacsimileTelephoneNumber() {
		return facsimileTelephoneNumber;
	}

	public void setFacsimileTelephoneNumber(String facsimileTelephoneNumber) {
		this.facsimileTelephoneNumber = facsimileTelephoneNumber;
	}

	public String getTelephoneNumber() {
		return telephoneNumber;
	}

	public void setTelephoneNumber(String telephoneNumber) {
		this.telephoneNumber = telephoneNumber;
	}

	public String getPasswordModifiedDate() {
		return passwordModifiedDate;
	}

	public void setPasswordModifiedDate(String passwordModifiedDate) {
		this.passwordModifiedDate = passwordModifiedDate;
	}

}
